import SwiftUI

/// Mint-inspired design system
enum Theme {
    enum Colors {
        static let primary = Color(hex: "00D09C")
        static let background = Color(hex: "F7F8FA")
        static let textPrimary = Color(hex: "2E3E4E")
        static let textSecondary = Color(hex: "6B7280")
        static let income = Color(hex: "10B981")
        static let expense = Color(hex: "EF4444")
    }

    enum Spacing {
        static let sm: CGFloat = 12
        static let md: CGFloat = 16
        static let lg: CGFloat = 24
    }
}

extension Color {
    init(hex: String) {
        let hex = hex.trimmingCharacters(in: CharacterSet.alphanumerics.inverted)
        var int: UInt64 = 0
        Scanner(string: hex).scanHexInt64(&int)
        let a, r, g, b: UInt64
        switch hex.count {
        case 6: (a, r, g, b) = (255, int >> 16, int >> 8 & 0xFF, int & 0xFF)
        default: (a, r, g, b) = (255, 0, 0, 0)
        }
        self.init(
            .sRGB,
            red: Double(r) / 255,
            green: Double(g) / 255,
            blue: Double(b) / 255,
            opacity: Double(a) / 255
        )
    }
}
